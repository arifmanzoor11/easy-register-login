

    <h1>How to Obtain a Facebook App ID and Secret Key</h1>

    <h2>Introduction</h2>
    <p>To integrate your application with Facebook services, you need a <strong>Facebook App ID</strong> and a <strong>Secret Key</strong>. These credentials allow your app to authenticate and interact with Facebook's APIs securely.</p>

    <h2>Steps to Get a Facebook App ID and Secret Key</h2>

    <h3>1. Create a Facebook Developer Account</h3>
    <p>Before creating an app, ensure you have a developer account:</p>
    <ul>
        <li>Visit the <a href="https://developers.facebook.com/" target="_blank">Meta for Developers</a> website.</li>
        <li>Sign in using your Facebook account.</li>
        <li>If you don’t have a developer account, you’ll be prompted to set one up.</li>
        <li>Accept the platform terms and conditions.</li>
    </ul>

    <h3>2. Create a Facebook App</h3>
    <p>Once your developer account is ready, create a new app:</p>
    <ul>
        <li>Go to the <a href="https://developers.facebook.com/apps/" target="_blank">Facebook App Creation Page</a>.</li>
        <li>Click <strong>Create App</strong>.</li>
        <li>Select the type of app you are creating (e.g., Business, Consumer, Gaming, etc.).</li>
        <li>Enter a name for your app and your email address.</li>
        <li>Click <strong>Create App ID</strong>.</li>
        <li>Complete any security verification steps if prompted.</li>
    </ul>

    <h3>3. Verify Your Business Account</h3>
    <p>If you are using Facebook APIs for business purposes, you may need to verify your account:</p>
    <ul>
        <li>Go to the <a href="https://business.facebook.com/" target="_blank">Facebook Business Manager</a>.</li>
        <li>Navigate to <strong>Business Settings</strong>.</li>
        <li>Under <strong>Security Center</strong>, click <strong>Start Verification</strong>.</li>
        <li>Provide required business details such as legal name, address, and website.</li>
        <li>Upload necessary documents for verification.</li>
        <li>Facebook will review and approve your request, which may take a few days.</li>
    </ul>
    <p>For more information, visit the <a href="https://www.facebook.com/business/help/2058515294227817" target="_blank">Business Verification Guide</a>.</p>

    <h3>4. Retrieve the App ID and Secret Key</h3>
    <p>Once your app is created, you can find the credentials by:</p>
    <ul>
        <li>Navigating to the <strong>App Dashboard</strong>.</li>
        <li>Clicking <strong>Settings</strong> on the left sidebar.</li>
        <li>Selecting <strong>Basic</strong> from the dropdown.</li>
        <li>Your <strong>App ID</strong> will be displayed.</li>
        <li>Click <strong>Show</strong> next to <strong>App Secret</strong> to view it (you may need to re-enter your password for security verification).</li>
    </ul>

    <h3>5. Secure Your App Secret</h3>
    <ul>
        <li><strong>Never share your App Secret</strong> in public repositories or with unauthorized individuals.</li>
        <li>Store it securely using environment variables or secret management services.</li>
        <li>If your App Secret is compromised, regenerate it from the settings page.</li>
    </ul>

